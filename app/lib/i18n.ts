const LANG_KEY = 'dome:language';

function getInitialLanguage(): string {
  try {
    const stored = localStorage.getItem(LANG_KEY);
    if (stored && ['en', 'es', 'fr', 'pt'].includes(stored)) return stored;
  } catch { /* ignore */ }
  return 'es';
}

