export function changeLanguage(lang: string): void {
  i18n.changeLanguage(lang);
  try {
    localStorage.setItem(LANG_KEY, lang);
  } catch { /* ignore */ }
}

export function getCurrentLanguage(): string {
  return i18n.language || 'es';
}

